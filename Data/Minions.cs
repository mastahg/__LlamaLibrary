public static class Minions
{
    public static List<MinionStore> Cards { get; } = MinionData.Minions;

    public static MinionStore GetMinionById(uint id)
    {
        return Cards.Find(x => x.Id == id);
    }

    public static MinionStore GetMinionByName(string name)
    {
        return Cards.Find(x => x.Name == name);
    }

    public static MinionStore GetMinionByItemID(uint id)
    {
        return Cards.Find(x => x.ItemId == id);
    }
}

public record MinionStore
{
    public MinionStore()
    {
    }

    public MinionStore(int id, string name, uint itemId)
    {
        Id = id;
        Name = name;
        ItemId = itemId;
    }

    public int Id { get; set; }
    public string Name { get; set; }
    public uint ItemId { get; set; }
    public string ItemName => DataManager.GetItem(ItemId).CurrentLocaleName;

    public bool IsUnlocked => UIState.MinionUnlocked(Id);

    public bool HaveItem => InventoryManager.FilledSlots.Any(i => i.RawItemId == ItemId);

    public override string ToString()
    {
        return $"{Id} - {Name} - {ItemName}({ItemId})";
    }
}

